char	*ft_strcat(char *s1, const char *s2)
{
	char	*l_s1;

	l_s1 = s1;
	while (*l_s1++ != '\0')
		;
	--l_s1;
	while (*s2 != '\0')
		(*l_s1++ = *s2++);
	*l_s1 = *s2;
	return (s1);
}
