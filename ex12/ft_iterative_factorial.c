int	ft_iterative_factorial(int nb)
{
	int	res;

	res = nb;
	if (nb == 0 || nb == 1)
		res = 1;
	while (nb > 1)
	{
		res = res * (nb - 1);
		nb--;
	}
	return (res);
}
